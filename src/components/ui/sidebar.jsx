const SIDEBAR_COOKIE_NAME = "sidebar_state"
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7
const SIDEBAR_WIDTH = "16rem"
const SIDEBAR_WIDTH_MOBILE = "18rem"
const SIDEBAR_WIDTH_ICON = "3rem"
const SIDEBAR_KEYBOARD_SHORTCUT = "b"

const SidebarContext = React.createContext(null)

function useSidebar() {
  const context = React.useContext(SidebarContext)
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider.")
  }

  return context
}

const SidebarProvider = React.forwardRef(
  (
    {
      defaultOpen = true,
      open: openProp,
      onOpenChange: setOpenProp,
      className,
      style,
      children,
      ...props
    },
    ref
  ) => {
    const isMobile = useIsMobile()
    const [openMobile, setOpenMobile] = React.useState(false)

    // Read stored state synchronously using lazy initializer to prevent flicker
    // This runs only once on mount and avoids the visible state transition
    const getStoredState = React.useCallback(() => {
      if (typeof window === 'undefined') return defaultOpen
      try {
        // Try localStorage first
        const stored = localStorage.getItem(SIDEBAR_COOKIE_NAME)
        if (stored !== null) {
          return stored === 'true'
        }
        // Fallback to cookie
        const cookies = document.cookie.split(';')
        const sidebarCookie = cookies.find(c => c.trim().startsWith(`${SIDEBAR_COOKIE_NAME}=`))
        if (sidebarCookie) {
          const value = sidebarCookie.split('=')[1]
          return value === 'true'
        }
      } catch {
        // localStorage might be unavailable
      }
      return defaultOpen
    }, [defaultOpen])

    // Use lazy initializer to read state synchronously on first mount
    // This prevents the flash from defaultOpen -> storedState
    const [_open, _setOpen] = React.useState(() => {
      if (typeof window === 'undefined') return defaultOpen
      return getStoredState()
    })

    // Track if we've hydrated (for handling isMobile undefined state)
    const [isHydrated, setIsHydrated] = React.useState(false)

    React.useEffect(() => {
      setIsHydrated(true)
    }, [])

    // Use stored state, or fallback to prop, or internal state
    const open = openProp ?? _open

    const setOpen = React.useCallback(
      (value) => {
        const openState = typeof value === "function" ? value(open) : value
        if (setOpenProp) {
          setOpenProp(openState)
        } else {
          _setOpen(openState)
        }

        // Save to both localStorage (for sync access) and cookie
        try {
          localStorage.setItem(SIDEBAR_COOKIE_NAME, String(openState))
          document.cookie = `${SIDEBAR_COOKIE_NAME}=${openState}; path=/; max-age=${SIDEBAR_COOKIE_MAX_AGE}`
        } catch {
          // localStorage might be unavailable
        }
      },
      [setOpenProp, open]
    )

    const toggleSidebar = React.useCallback(() => {
      // Only toggle mobile if isMobile is determined and true
      return isMobile === true
        ? setOpenMobile((open) => !open)
        : setOpen((open) => !open)
    }, [isMobile, setOpen, setOpenMobile])

    React.useEffect(() => {
      const handleKeyDown = (event) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault()
          toggleSidebar()
        }
      }

      window.addEventListener("keydown", handleKeyDown)
      return () => window.removeEventListener("keydown", handleKeyDown)
    }, [toggleSidebar])

    const state = open ? "expanded" : "collapsed"

    const contextValue = React.useMemo(
      () => ({
        state,
        open,
        setOpen,
        isMobile: isMobile ?? false, // Default to false while determining
        openMobile,
        setOpenMobile,
        toggleSidebar,
        isHydrated,
      }),
      [state, open, setOpen, isMobile, openMobile, setOpenMobile, toggleSidebar, isHydrated]
    )

    // During SSR or before hydration, render a placeholder that matches the hydrated structure
    // but with visibility hidden to prevent layout shift while avoiding hydration mismatch
    if (!isHydrated) {
      return (
        <div
          style={{
            "--sidebar-width": SIDEBAR_WIDTH,
            "--sidebar-width-icon": SIDEBAR_WIDTH_ICON,
            ...style,
          }}
          className={cn(
            "group/sidebar-wrapper flex min-h-svh w-full max-w-full overflow-x-hidden has-[[data-variant=inset]]:bg-sidebar",
            className
          )}
          ref={ref}
          suppressHydrationWarning
          {...props}
        >
          {/* Render children during SSR but the context will provide stable defaults */}
        </div>
      )
    }

    return (
      <SidebarContext.Provider value={contextValue}>
        <TooltipProvider delayDuration={0}>
          <div
            style={{
              "--sidebar-width": SIDEBAR_WIDTH,
              "--sidebar-width-icon": SIDEBAR_WIDTH_ICON,
              ...style,
            }}
            className={cn(
              "group/sidebar-wrapper flex min-h-svh w-full max-w-full overflow-x-hidden has-[[data-variant=inset]]:bg-sidebar",
              className
            )}
            ref={ref}
            {...props}
          >
            {children}
          </div>
        </TooltipProvider>
      </SidebarContext.Provider>
    )
  }
)
SidebarProvider.displayName = "SidebarProvider"

const Sidebar = React.forwardRef(
  (
    {
      side = "left",
      variant = "sidebar",
      collapsible = "offcanvas",
      className,
      children,
      ...props
    },
    ref
  ) => {
    const { isMobile, state, openMobile, setOpenMobile } = useSidebar()

    if (collapsible === "none") {
      return (
        <div
          className={cn(
            "flex h-full w-[--sidebar-width] flex-col text-sidebar-foreground",
            className
          )}
          style={{ backgroundColor: 'var(--sidebar)' }}
          ref={ref}
          {...props}
        >
          {children}
        </div>
      )
    }

    if (isMobile) {
      return (
        <Sheet open={openMobile} onOpenChange={setOpenMobile} {...props}>
          <SheetContent
            data-sidebar="sidebar"
            data-mobile="true"
            className="w-[--sidebar-width] p-0 text-sidebar-foreground [&>button]:hidden"
            style={{
              "--sidebar-width": SIDEBAR_WIDTH_MOBILE,
              backgroundColor: 'var(--sidebar)',
            }}
            side={side}
          >
            <SheetTitle className="sr-only">Navigation Menu</SheetTitle>
            <SheetDescription className="sr-only">
              Main navigation sidebar
            </SheetDescription>
            <div className="flex h-full w-full flex-col">{children}</div>
          </SheetContent>
        </Sheet>
      )
    }

    return (
      <div
        ref={ref}
        className="group peer hidden md:flex shrink-0 text-sidebar-foreground"
        data-state={state}
        data-collapsible={state === "collapsed" ? collapsible : ""}
        data-variant={variant}
        data-side={side}
      >
        {/* Spacer div to push content over */}
        <div
          className="relative h-svh bg-transparent shrink-0"
          style={{
            width: state === "collapsed"
              ? (collapsible === "icon" ? 'var(--sidebar-width-icon)' : '0px')
              : 'var(--sidebar-width)'
          }}
        />
        {/* Fixed sidebar container */}
        <div
          className={cn(
            "fixed inset-y-0 z-50 hidden h-svh md:flex overflow-hidden",
            side === "left"
              ? "left-0"
              : "right-0",
            "group-data-[side=left]:border-r group-data-[side=right]:border-l",
            className
          )}
          style={{
            backgroundColor: 'var(--sidebar)',
            width: state === "collapsed"
              ? (collapsible === "icon" ? 'var(--sidebar-width-icon)' : '0px')
              : 'var(--sidebar-width)'
          }}
          {...props}
        >
          <div
            data-sidebar="sidebar"
            className="flex h-full w-full flex-col overflow-hidden group-data-[variant=floating]:rounded-lg group-data-[variant=floating]:border group-data-[variant=floating]:border-sidebar-border group-data-[variant=floating]:shadow"
            style={{ backgroundColor: 'var(--sidebar)' }}
          >
            {children}
          </div>
        </div>
      </div>
    )
  }
)
Sidebar.displayName = "Sidebar"

